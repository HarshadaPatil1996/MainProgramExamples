package allprograms;

public class for_loop {
public static void main(String[] args)
{

	//start num: 1
	//end num:   5
	//incr/decr: incr by 1
	 
	  //    1    6<=5    6
	for(int i=1; i<=5; i++)                              //i++ increase by 1
	{
		System.out.println(i);                            //i+2 increase by 2   and so on
	}
	
	//1
	//2
	//3
	//4
	//5
}
}
