package array;

public class smaple5
{

	public static void main(String[] args)
	{
		
		// 3X3 3rows x 3 column 

		int [][] a= {{10,10,10},{20,20,20},{30,30,30}};
		
		
		for (int i=0;i<=2;i++)
		{
			for(int j=0;j<=2;j++)
			{
				System.out.print(a[i][j]+" ");
			}
			System.out.println();
		}
		
		
		 
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
